CREATE TABLE ticket_assignments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticket_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    assigned_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ta_ticket FOREIGN KEY (ticket_id) REFERENCES tickets(id) ON DELETE CASCADE,
    CONSTRAINT fk_ta_user FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT uq_ta_ticket_user UNIQUE (ticket_id, user_id)
);

INSERT INTO ticket_assignments (ticket_id, user_id, assigned_at)
SELECT t.id, t.assigned_to, CURRENT_TIMESTAMP
FROM tickets t
WHERE t.assigned_to IS NOT NULL;

CREATE INDEX idx_ta_ticket ON ticket_assignments(ticket_id);
CREATE INDEX idx_ta_user ON ticket_assignments(user_id);
