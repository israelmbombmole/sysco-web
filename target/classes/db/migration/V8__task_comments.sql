CREATE TABLE task_comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_id BIGINT NOT NULL,
    author_user_id BIGINT,
    comment_text CLOB NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_tcom_task FOREIGN KEY (task_id) REFERENCES automated_jobs(id) ON DELETE CASCADE,
    CONSTRAINT fk_tcom_author FOREIGN KEY (author_user_id) REFERENCES users(id)
);

CREATE INDEX idx_tcom_task ON task_comments(task_id, created_at);
