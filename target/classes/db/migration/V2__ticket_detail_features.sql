CREATE TABLE ticket_comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticket_id BIGINT NOT NULL,
    author_user_id BIGINT,
    comment_text CLOB NOT NULL,
    attachment_path VARCHAR(4000),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_tc_ticket FOREIGN KEY (ticket_id) REFERENCES tickets(id) ON DELETE CASCADE,
    CONSTRAINT fk_tc_author FOREIGN KEY (author_user_id) REFERENCES users(id)
);

CREATE TABLE ticket_events (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ticket_id BIGINT NOT NULL,
    event_type VARCHAR(64) NOT NULL,
    actor_user_id BIGINT,
    target_user_id BIGINT,
    note CLOB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_te_ticket FOREIGN KEY (ticket_id) REFERENCES tickets(id) ON DELETE CASCADE,
    CONSTRAINT fk_te_actor FOREIGN KEY (actor_user_id) REFERENCES users(id),
    CONSTRAINT fk_te_target FOREIGN KEY (target_user_id) REFERENCES users(id)
);

CREATE INDEX idx_ticket_comments_ticket ON ticket_comments(ticket_id, created_at);
CREATE INDEX idx_ticket_events_ticket ON ticket_events(ticket_id, created_at);
