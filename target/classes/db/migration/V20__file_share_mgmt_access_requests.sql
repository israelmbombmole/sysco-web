CREATE TABLE file_share_mgmt_access_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    requester_user_id BIGINT NOT NULL,
    direction_id BIGINT,
    status VARCHAR(32) NOT NULL,
    otp_code VARCHAR(32),
    otp_valid_until TIMESTAMP,
    session_minutes INT NOT NULL DEFAULT 5,
    issued_by_user_id BIGINT,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_fsmar_requester FOREIGN KEY (requester_user_id) REFERENCES users(id),
    CONSTRAINT fk_fsmar_direction FOREIGN KEY (direction_id) REFERENCES directions(id),
    CONSTRAINT fk_fsmar_issuer FOREIGN KEY (issued_by_user_id) REFERENCES users(id)
);

CREATE INDEX idx_fsmar_direction_status ON file_share_mgmt_access_requests(direction_id, status);
CREATE INDEX idx_fsmar_requester_status ON file_share_mgmt_access_requests(requester_user_id, status);
