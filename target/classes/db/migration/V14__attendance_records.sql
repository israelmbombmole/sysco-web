-- Persistent attendance / presence sessions (Ma garde)

CREATE TABLE attendance_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id),
    direction_name VARCHAR(512) NOT NULL,
    arrival_at TIMESTAMP NOT NULL,
    departure_at TIMESTAMP,
    created_at TIMESTAMP NOT NULL
);

CREATE INDEX idx_attendance_arrival ON attendance_records(arrival_at);
CREATE INDEX idx_attendance_user_arrival ON attendance_records(user_id, arrival_at);
CREATE INDEX idx_attendance_user_dep ON attendance_records(user_id, departure_at);
