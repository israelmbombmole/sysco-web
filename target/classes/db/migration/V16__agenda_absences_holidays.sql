-- Agenda: persistent absences / leave entries and organization-wide public holidays.

CREATE TABLE agenda_absences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    absence_type VARCHAR(64) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    remarks VARCHAR(4000),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by BIGINT,
    CONSTRAINT fk_agenda_abs_user FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT fk_agenda_abs_creator FOREIGN KEY (created_by) REFERENCES users(id)
);

CREATE INDEX idx_agenda_abs_dates ON agenda_absences(start_date, end_date);
CREATE INDEX idx_agenda_abs_user ON agenda_absences(user_id);

CREATE TABLE agenda_public_holidays (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    label VARCHAR(4000) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_agenda_holiday_dates ON agenda_public_holidays(start_date, end_date);
