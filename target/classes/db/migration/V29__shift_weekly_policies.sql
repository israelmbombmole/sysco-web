-- Weekly punch windows per sous-direction (Ma garde) + same-day overrides

CREATE TABLE shift_weekly_policies (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    week_monday DATE NOT NULL,
    sous_direction_id BIGINT NOT NULL,
    arrival_allowed_from TIME NOT NULL,
    arrival_on_time_until TIME NOT NULL,
    arrival_late_until TIME NOT NULL,
    departure_allowed_from TIME NOT NULL,
    departure_allowed_until TIME NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    updated_by_user_id BIGINT,
    CONSTRAINT fk_shift_pol_sd FOREIGN KEY (sous_direction_id) REFERENCES sous_directions(id),
    CONSTRAINT fk_shift_pol_user FOREIGN KEY (updated_by_user_id) REFERENCES users(id),
    CONSTRAINT uq_shift_policy_week_sd UNIQUE (week_monday, sous_direction_id)
);

CREATE TABLE shift_punch_day_overrides (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    for_date DATE NOT NULL,
    user_id BIGINT NOT NULL,
    granted_by_user_id BIGINT NOT NULL,
    created_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_shift_ov_user FOREIGN KEY (user_id) REFERENCES users(id),
    CONSTRAINT fk_shift_ov_grant FOREIGN KEY (granted_by_user_id) REFERENCES users(id),
    CONSTRAINT uq_shift_ov_day_user UNIQUE (for_date, user_id)
);

CREATE INDEX idx_shift_ov_date ON shift_punch_day_overrides(for_date);

ALTER TABLE attendance_records ADD COLUMN late_arrival BOOLEAN NOT NULL DEFAULT FALSE;
ALTER TABLE attendance_records ADD COLUMN outside_window_override BOOLEAN NOT NULL DEFAULT FALSE;
